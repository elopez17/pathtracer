#include <pt.h>

void	draw(t_rt *rt)
{
	mlx_do_sync(rt->mlx);
	mlx_clear_window(rt->mlx, rt->win);
	mlx_put_image_to_window(rt->mlx, rt->win, rt->img, 0, 0);
}
